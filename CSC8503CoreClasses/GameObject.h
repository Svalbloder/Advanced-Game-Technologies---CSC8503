	class GameObject	{
	public:
		GameObject(const std::string& name = "");
		~GameObject();

		void SetBoundingVolume(CollisionVolume* vol) {
			boundingVolume = vol;
		}

		const CollisionVolume* GetBoundingVolume() const {
			return boundingVolume;
		}

		bool IsActive() const {
			return isActive;
		}

		Transform& GetTransform() {
			return transform;
		}

		RenderObject* GetRenderObject() const {
			return renderObject;
		}

		PhysicsObject* GetPhysicsObject() const {
			return physicsObject;
		}

		NetworkObject* GetNetworkObject() const {
			return networkObject;
		}

		void SetRenderObject(RenderObject* newObject) {
			renderObject = newObject;
		}

		void SetPhysicsObject(PhysicsObject* newObject) {
			physicsObject = newObject;
		}

		const std::string& GetName() const {
			return name;
		}

		virtual void OnCollisionBegin(GameObject* otherObject) {
			//std::cout << "OnCollisionBegin event occured!\n";
		}

		virtual void OnCollisionEnd(GameObject* otherObject) {
			//std::cout << "OnCollisionEnd event occured!\n";
		}

		bool GetBroadphaseAABB(Vector3&outsize) const;

		void UpdateBroadphaseAABB();

		void SetWorldID(int newID) {
			worldID = newID;
		}

		int		GetWorldID() const {
			return worldID;
		}
		bool triggerDelete;

	protected:
		Transform			transform;

		CollisionVolume*	boundingVolume;
		PhysicsObject*		physicsObject;
		RenderObject*		renderObject;
		NetworkObject*		networkObject;

		bool		isActive;
		int			worldID;
		std::string	name;

		Vector3 broadphaseAABB;
	};
	class ChildGameObject : public GameObject {
	public:
		ChildGameObject(std::string name = "", GameObject* parent = nullptr)
			: GameObject(name), parentObject(parent) {
		}

		void SetParent(GameObject* parent) {
			parentObject = parent;
		}

		GameObject* GetParent() const {
			return parentObject;
		}

		// Override the GetTransform() function to consider the parent's transform
		Transform& GetTransform() {
			if (parentObject) {
				Transform combinedTransform = parentObject->GetTransform();
				combinedTransform.SetPosition(combinedTransform.GetPosition() + transform.GetPosition());
				// You can add more adjustments here if needed (rotation, scale, etc.)
				return combinedTransform;
			}
			return transform;

		}

	private:
		GameObject* parentObject;
	};
